#include <iostream> 
// Incluye la librería iostream, necesaria para usar entrada y salida estándar
// como std::cout (imprimir en pantalla)

int main() { 
    // Función principal del programa: aquí empieza la ejecución

    int a = 75 - 9;
    // Se declara una variable entera llamada 'a'
    // Se le asigna el resultado de la resta: 75 - 9 = 66

    int b = 50 - 25;
    // Se declara una variable entera llamada 'b'
    // Se le asigna el resultado de la resta: 50 - 25 = 25

    std::cout << a - b;
    // Se imprime en pantalla el resultado de la resta entre 'a' y 'b'
    // 66 - 25 = 41
}
